    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        usuario=recibeusuario.getText();
        pass=recibepass.getText();
        
        logindao l=new logindao();
        
        if(!"".equals(usuario) && !"".equals(pass) ){
        String res=l.aceso(usuario, pass);
        String nombre=l.obtennombre(usuario, pass);
        vendedor=l.obtenid(usuario,pass);
        switch (res){
        
            case "1":
                JOptionPane.showMessageDialog(null,"Bienvenido administrador "+nombre);
                VistaAdministrador ad=new VistaAdministrador();
                ad.setVisible(true);
                ad.pack();
                Login.this.setVisible(false);
                break;
                
            case "2":
                JOptionPane.showMessageDialog(null,"Bienvenido Oficial "+nombre);
                VistaOficial oc=new VistaOficial();
           
                oc.setVisible(true);
                oc.pack();
                Login.this.setVisible(false);
                break;
                
             case "3":
                JOptionPane.showMessageDialog(null,"Bienvenido vendedor "+nombre);
                VistaVendedor v=new VistaVendedor();
                v.setVisible(true);
                v.pack();
                Login.this.setVisible(false);
                break;
                 
             default:
                 JOptionPane.showMessageDialog(null,"Los datos ingresados no existen");
        }
        }
        else{
        JOptionPane.showMessageDialog(null,"Datos faltantes en los campos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

